# declaração das variaveis do programa
# a, b, c são declaradas na atribuição de valores

#abtriu o valor 0 à variavel d
d = 0
# exibe na tela o valor de d  com frase explicativa
print("O Valor de D:", d)

#abtriu o valor 0 à variavel a
a = 2
# exibe na tela o valor de a  com frase explicativa
print("O Valor de A:", a)

#abtriu o valor 0 à variavel b
b = 4
# exibe na tela o valor de b  com frase explicativa
print("O Valor de B:", b)

#abtriu o valor 0 à variavel c
c = 6
# exibe na tela o valor de c  com frase explicativa
print("O Valor de C:", c)

# Atualizar a variavel C o seu valor, acrescentando 
c = c + 1 # + operador de adição
print("O Valor de C:", c)

#Atribuir o valor de D somando com C + A
d = c + a
print("O Valor de D:", d)

