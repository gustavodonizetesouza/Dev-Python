x = 5
print(x > 3 and x < 10) # irá retornar verdadeiro se as duas expressoes retorna verdadeiro. Expressão END

print(x > 3 or x < 4) # irá retornar verdadeiro se as apenas uma das expressoes retorna verdadeiro. Expressão OR

print(not(x > 3 and x < 10)) # Expressão que invert o retorno da experessa. Se retornar verdadeira retorna falso e vice-verso.
