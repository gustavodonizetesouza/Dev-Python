print('------------Tipos Inteiros -----------------------')

a = 1 # inteiro
b = 2500 # inteiro
c = -1 # inteiro negativo

print(type(a))
print(type(b))
print(type(c))

print('------------Tipos Float -----------------------')

d = 1.1 # float
e = 1.0 # float
f = -1.0 # float

print(type(d))
print(type(e))
print(type(f))

print('-------------Tipos Float ----------------------')

g = 35e3
h = 12E4
i = -87.7e100

print(type(g))
print(type(h))
print(type(i))

print('------------Tipos Complex -----------------------')

j = 3+5j
